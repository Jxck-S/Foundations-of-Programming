# Logical Operators

def main():

    answer = input("Which frozen treat do you like? A) Ice cream or B) Popsicles")

    if( answer == "a" or answer == "A" ):
        print("  o, ")
        print(" (~ ) ")
        print(" \~~/ ")
        print("  \/")
        print(" YUMMY!! ")
    elif( answer == "b" or answer == "B" ):
        print("    ___ ")
        print("   // //\ ")
        print("  // // /| ")
        print("  // // // ")
        print(" ( // // ")
        print("  \_/ // ")
        print(" / /\_/  ")
        print(" (_/  Mmm, good!")

main()